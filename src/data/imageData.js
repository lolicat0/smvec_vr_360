// Helper function to get all images by category
export const getImagesByCategory = (category) => {
  if (category === 'campus') {
    return imageData.campus;
  }
  
  if (category.startsWith('engineering-')) {
    const dept = category.replace('engineering-', '');
    return imageData.engineering[dept] || [];
  }
  
  return imageData[category] || [];
};

// Helper function to get all engineering departments
export const getEngineeringDepartments = () => {
  return Object.keys(imageData.engineering);
};

